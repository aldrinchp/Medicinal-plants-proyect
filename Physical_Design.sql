                ID_RESEARCH_PK INTEGER NOT NULL,
                ID_PLANT_PK INTEGER NOT NULL,
                TITLE VARCHAR(400) NOT NULL,
                LINK VARCHAR(400),
                CONSTRAINT ID_RESEARCH_PK PRIMARY KEY (ID_RESEARCH_PK, ID_PLANT_PK)
);


CREATE TABLE USE (
                ID_USE_PK INTEGER NOT NULL,
                ID_PLANT_PK INTEGER NOT NULL,
                DESCRIPTION VARCHAR(1750) NOT NULL,
                TYPE_USE VARCHAR(100) NOT NULL,
                CONSTRAINT ID_USE_PK PRIMARY KEY (ID_USE_PK, ID_PLANT_PK)
);


CREATE TABLE TOPONIMO (
                ID_TOPONIMO INTEGER NOT NULL,
                ID_PLANT_PK INTEGER NOT NULL,
                ORIGIN VARCHAR(250),
                COMMERCIAL_DISTRIBUTION VARCHAR(250),
                DISTRIBUTION VARCHAR(250),
                CONSTRAINT ID_TOPONIMO PRIMARY KEY (ID_TOPONIMO, ID_PLANT_PK)
);


CREATE TABLE IMAGE (
                ID_IMAGE_PK INTEGER NOT NULL,
                ID_PLANT_PK INTEGER NOT NULL,
                LINK VARCHAR(400) NOT NULL,
                DATE DATE NOT NULL,
                PLACE VARCHAR(250) NOT NULL,
                CONSTRAINT ID_IMAGE_PK PRIMARY KEY (ID_IMAGE_PK, ID_PLANT_PK)
);


