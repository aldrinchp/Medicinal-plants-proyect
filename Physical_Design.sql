CREATE TABLE PLANTS (
                ID_PLANT_PK INTEGER NOT NULL,
                ID_LOCATION INTEGER NOT NULL,
                CURRENT_NAME VARCHAR(200) NOT NULL,
                SCIENTIFIC_NAME VARCHAR(200) NOT NULL,
                CONSTRAINT ID_PLANT_PK PRIMARY KEY (ID_PLANT_PK, ID_LOCATION)
);


